class SquareMaking
{
public:
	int getMinimalPrice(int a, int b, int c, int d) {
		vector<int> A;
		A.pb(a);A.pb(b);A.pb(c);A.pb(d);
		sort(A.begin(), A.end());
		int ans = 0;
		ans += (A[1] - A[0]) + (A[3] - A[2]);
		int mid = (A[1] + A[2])/2;
		ans += 2*(mid - A[1]) + 2*(A[2] - mid);
		return ans;
	}
};